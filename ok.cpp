#include <iostream>
using namespace std;
int main(){
    //last and final day of operators
      //Arithmetic operators//
    int num1=6;
    int num2 =7;
    cout<<(num1+num2)<<endl;//13
    cout<<(num1-num2)<<endl;//-1
    cout<<(num1*num2)<<endl;//42
    cout<<(num1%num2)<<endl;
    cout<<(num1/num2)<<endl;


   //relational operators
   int num3=8;
   int num4=6;
   cout<<(num1==num2)<<endl;//0
   cout<<(num1!=num2)<<endl;//1

   //logical operators
   int exp1=2;
   int exp2=7;
   cout<<(exp1&&exp2)<<endl;//1
   cout<<(exp1||exp2)<<endl;//1
   
       return 0;
}




  
  
